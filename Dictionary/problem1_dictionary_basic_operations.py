# Exercise 1: Dictionary operations

# logic
# Update → student["age"] = 21
# Add → student["city"] = "New York"
# Access → student["name"]

student = {"name": "Alice", "age": 20, "grade": "B"}

# modify existing value
student["age"] = 21

# add new key-value pair
student["city"] = "New York"

# access specific key
print(student)
print("Name:", student["name"])